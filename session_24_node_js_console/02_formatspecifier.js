// Format specifiers are a powerful tool for formatting strings in Node.js. 
// They can be used to create simple or complex formatted strings, and they can be used to format numbers according to a specific locale.

console.log('Hello %s', 'Jone'); // %s specifier indicates that a string will be inserted
console.log('Hello %d', 49); // %d specifier indicates that a number will be inserted